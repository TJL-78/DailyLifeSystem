export function useI18n() {
  function t(key) {
    return (I18N[currentLang.value] || I18N.zh)[key] || key
  }
  function switchLang(lang) {
    currentLang.value = lang
    localStorage.setItem('lang', lang)
  }
  return { t, currentLang, switchLang }
}
